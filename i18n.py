def translate(lang: str, key: str, default: str | None = None) -> str:
    lang = (lang or DEFAULT_LANG).lower()
    if lang not in MESSAGES:
        lang = DEFAULT_LANG
    return MESSAGES[lang].get(key) or MESSAGES[DEFAULT_LANG].get(key) or (default if default is not None else key)
